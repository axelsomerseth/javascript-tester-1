function addition(a, b) {
  return a + b;
}

function substact(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function exponentiation(a, b) {
  return a ** b;
}

module.exports = {
  addition,
  substact,
  multiply,
  divide,
  exponentiation,
};
